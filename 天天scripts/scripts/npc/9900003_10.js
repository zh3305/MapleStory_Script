function action(mode, type, selection) {
    if (mode == -1) {
        cm.dispose();
    } else {
        if (mode == 1)
            a++;
        else
            a--;
        if (a == -1) {
            cm.dispose();
        } else if (a == 0) {
            text = "#h0#,你好！请问需要些什么呢?\r\n#b";
            for (var i = 0; i < itemname.length; i++) {
                // text += "#L" + i + "#" + itemname[i] + "#  \r\n";
                text += "#L" + i + "#" + itemname[i] + "#l";
                if (i != 0 && (i + 1) % 5 == 0) {
                    text += "\r\n";
                }
            }
            cm.sendSimple(text);

        } else if (a == 1) {
            text = "#h0#,你好！在这里可以选择你想要购买的物品,点击购买#b";
            if (selection != null && selection != -1) {
                selectt = selection;
            }
            // cm.worldSpouseMessage(0x20, selectt+"  "+selection);
            for (var i = 0; i < Itemlist2[selectt].length; i++) {

                //text += "#L" + i + "##i" + Itemlist2[selectt][i] + "# #b#t" + Itemlist2[selectt][i] + "##k   #r" + Itemlist2[selectt][i][2];

                text += "\r\n#L" + i + "##i" + Itemlist2[selectt][i] + ":# #b#t" + Itemlist2[selectt][i] + "##k   #r" + Itemlist2[selectt][i][2];

                if (Itemlist2[selectt][i][1] == 1) {
                    // text += "#k点 #l \r\n";
                    text += "#k点卷#l";
                } else {
                    // text += "#k抵用券 #l \r\n";
                    text += "#k抵用券#l";

                }
                // text += "#L" + i + "#i" + Itemlist2[selectt][i]  + "#  ";
                // if (i != 0 && (i + 1) % 5 == 0) {
                //     text += "\r\n";
                // }
                // text += "#L" + i + "##i" +  Itemlist2[selectt][i] + "#  ";
                // if (i != 0 && (i + 1) % 5 == 0) {
                //     text += "\r\n";
                // }
            }
            cm.sendNextPrev(text);

        } else if (a == 2) {
            if (selection != null && selection != -1) {
                selects = selection;
            }
            Mtype = Itemlist2[selectt][selects][1];
            if (Mtype == 1) {
                Mtypet = "点";
            } else {
                Mtypet = "抵用";
            }
            // cm.worldSpouseMessage(0x20, selects+"  "+selection);
            cm.sendGetNumber("请输入你请你输入想要购买的数量\r\n\r\n#r - 1个需要" + Itemlist2[selectt][selects][2] + Mtypet, 0, 0, 999999);
        } else if (a == 3) {
            if (selection != null && selection != -1) {
                buynum = selection;
            }
            cm.sendNextPrev("你想购买" + buynum + "个#r#i" + Itemlist2[selectt][selects][0] + "##k？\r\n你将使用掉" + (buynum * Itemlist2[selectt][selects][2]) + Mtypet + "。");
        } else if (a == 4) {
            if (cm.getChar().getCSPoints(Mtype) >= buynum * Itemlist2[selectt][selects][2]) {
                cm.getChar().modifyCSPoints(Mtype, -buynum * Itemlist2[selectt][selects][2]);
                cm.gainItem(Itemlist2[selectt][selects][0], buynum);
                cm.sendOk("购买成功了！");
                cm.dispose();
            } else {
                cm.sendOk("对不起，你没有足够的" + Mtypet + "。");
                cm.dispose();
            }
        }
    } //mode
} //f

